package Chapter2_ControlFlow;

public class Lesson3_WhileLoop
{
    public static void main(String[] args) {
        int a = 0;                      // initialization
        while (a<=9){                   // continuation
            System.out.print(a+" ");
            a++;                        // increment
        }

        int b = 10;
        while (b>=4) {
            System.out.println(b + " ");
            b--;                        //decrement
        }
        System.out.println("  ");
         int c = 0;
         while (c<=5){
             System.out.println(c + " ");
             c++;

        }
    }
}
