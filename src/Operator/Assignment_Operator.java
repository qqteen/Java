package Operator;

public class Assignment_Operator
{
    public static void main(String[] args)
    {
     int a = 5;
     int b = a+5;
        System.out.println(b);

     int c = 3;
     c = c+5;
        System.out.println(c);

     int d = 7;
     d += 3;
        System.out.println(d);

     int e = 8;
     e -= 5;
        System.out.println(e);
    }
}


